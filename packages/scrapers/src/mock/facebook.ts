export class FacebookScraper extends BaseScraper {
  readonly name = "facebook"
  readonly displayName = "Facebook Marketplace"
  readonly baseUrl = "https://www.facebook.com/marketplace"

  async search(query: SearchQuery): Promise<ScraperResult> {
    const start = Date.now()
    await this.delay(1000 + Math.random() * 500)

    const listings = MOCK_LISTINGS.filter((l) =>
      this.matchesQuery(l as BoatListing, query)
    ) as BoatListing[]

    return {
      source: this.name,
      listings,
      totalFound: listings.length,
      scrapedAt: new Date(),
      durationMs: Date.now() - start,
    }
  }
}
