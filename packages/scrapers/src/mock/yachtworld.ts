export class YachtWorldScraper extends BaseScraper {
  readonly name = "yachtworld"
  readonly displayName = "YachtWorld"
  readonly baseUrl = "https://www.yachtworld.com"

  async search(query: SearchQuery): Promise<ScraperResult> {
    const start = Date.now()
    await this.delay(700 + Math.random() * 900)

    const listings = MOCK_LISTINGS.filter((l) =>
      this.matchesQuery(l as BoatListing, query)
    ) as BoatListing[]

    return {
      source: this.name,
      listings,
      totalFound: listings.length,
      scrapedAt: new Date(),
      durationMs: Date.now() - start,
    }
  }
}
