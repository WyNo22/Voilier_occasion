export class BandOfBoatsScraper extends BaseScraper {
  readonly name = "bandofboats"
  readonly displayName = "Band of Boats"
  readonly baseUrl = "https://www.bandofboats.com"

  async search(query: SearchQuery): Promise<ScraperResult> {
    const start = Date.now()
    await this.delay(600 + Math.random() * 800)

    const listings = MOCK_LISTINGS.filter((l) =>
      this.matchesQuery(l as BoatListing, query)
    ) as BoatListing[]

    return {
      source: this.name,
      listings,
      totalFound: listings.length,
      scrapedAt: new Date(),
      durationMs: Date.now() - start,
    }
  }
}
